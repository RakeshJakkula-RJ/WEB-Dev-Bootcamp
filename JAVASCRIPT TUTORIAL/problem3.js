// Naming Conventions 

//1. Camel Case-----> to define variable names 
// ex.

let userName = "Robby";
let myFirstName = "Keene";

console.log(userName);

// 2. Snake Case----> variable 
//ex :

let user_name = "Migiuel";
let my_first_name = "Diaz";

console.log(user_name);

let MY_MOTHER_NAME = "carmen";  // we use all capital cases for constant values 

//3. Pascal case-------->variable 
// ex,

let UserName = "Hawk";
let MyFirstName = "Eli";

console.log(UserName);

