
// Rest operator : Rest operator is used for a function,
//  when we want to convert the arguments into a array(...b)

// Spread operator : spread operatror is used to create a copy of array, object(...)

// syntax for both spread operator and rest operator are same (...) but used in two different scenarios.

function print(...b){

    console.log(b);
};

