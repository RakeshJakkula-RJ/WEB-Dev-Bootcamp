// Ternary Operator 

// syntax : (condition) ? true statement : false statement;

// example 

let a = "139";

let b = 139;

(a==b) ? console.log("a and b are equal") : console.log("a and b are not equal");

let x = 9999;

let y = 999;

(x>=y) ? console.log("x is greather than y") : console.log("y is greater than x");


